<h2>Informações</h2>
<p>Centro de Reuso e Reciclagem de Tecnologia (CRRT) da UEMG, unidade de Passos foi criado em 2013 e atende toda a
  população da cidade e da região, visando a reutilização dos equipamentos que funcionam e o e descarte de maneira
  correta dos equipamentos estragados. O professor Fernando Roberto Proença, coordenador do Projeto e do Curso de Sistemas de
  Informação acrescenta que essa atividade promove a integração entre estudantes, professores e comunidade em geral, além de
  auxiliar na formação dos discentes. “Também proporciona aos estudantes a oportunidade de aplicarem na prática, os conceitos
  apresentados nas disciplinas do curso”. Para o professor Maílson de Queiroz Proença, coordenador do CRRT, a relevância está na
  conscientização da comunidade acadêmica e da sociedade da região sobre a importância do descarte adequado dos equipamentos eletrônicos.
  “Esses materiais possuem resíduos perigosos, que podem prejudicar o meio ambiente e a saúde das pessoas. Não devem ser descartados no lixo comum”.</p>
<h2>Processo</h2>
<p>O CRRT opera seguindo três etapas. A primeira etapa é a da coleta e triagem. Durante a
  recepção das peças e equipamentos é avaliada a possibilidade de reaproveitamento. Em caso positivo,
  são encaminhados para projetos sociais ou instituições de ensino. Os equipamentos que não tenham condições
  de serem reaproveitados são encaminhados para a segunda etapa, de categorização. Nessa etapa, tais equipamentos
  são pesados, desmontados e separados por tipo de material, como plásticos, metais, placas eletrônicas, cabos etc.
  Por último, os materiais categorizados são armazenados até o recolhimento por empresas de reciclagem. (FESP.EDU, 2017, online).</p>
