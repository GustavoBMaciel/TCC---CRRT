<h2>Bem vindo ao CRRT</h2>
<p>Esse site tem como objetivo divulgar e informar sobre o CRRT - Centro de Reuso e Reciclagem de tecnologia da UEMG
unidade de Passos, Minas Gerais.</p>
<h2>Informações</h2>
<p>O consumo de computadores e aparelhos de telecomunicações cresce em proporções
elevadas, esse aumento supera a capacidade de absorção do ambiente, esse tipo de material,
além de contaminar o meio ambiente, contém substâncias químicas que podem provocar doenças
graves em pessoas. A Universidade do Estado de Minas Gerais (UEMG) unidade Passos possui um
Centro de descarte, reuso e reciclagem de lixo eletrônico que visa o descarte correto de equipamentos
eletrônicos e a reutilização de peças em bom estado de uso com processos organizacionais estruturados. </p>
